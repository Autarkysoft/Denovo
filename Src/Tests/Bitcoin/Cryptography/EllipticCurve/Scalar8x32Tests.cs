        [Theory]
        [MemberData(nameof(GetLibsecp256k1_StaticCases))]
        public void Libsecp256k1_StaticTest(byte[] xba, byte[] yba, byte[] r1ba, byte[] r2ba)
        {
            Scalar8x32 x = new(xba, out bool overflow);
            Assert.False(overflow);
            Scalar8x32 y = new(yba, out overflow);
            Assert.False(overflow);
            Scalar8x32 r1 = new(r1ba, out overflow);
            Assert.False(overflow);
            Scalar8x32 r2 = new(r2ba, out overflow);
            Assert.False(overflow);

            Scalar8x32 z = x.Multiply(y);
            Assert.True(z.Verify());
            Assert.Equal(r1, z);

            if (!y.IsZero)
            {
                Scalar8x32 zz = y.Inverse();
                Assert.True(zz.Verify());
                Scalar8x32 zzv = y.InverseVar();
                Assert.Equal(zzv, zz);
                // x*y*y' == x*1 == x  (y' represents inverse of y)
                z = z.Multiply(zz);
                Assert.True(z.Verify());
                Assert.Equal(x, z);
                // y'*y == 1
                zz = zz.Multiply(y);
                Assert.True(zz.Verify());
                Assert.Equal(Scalar8x32.One, zz);
            }
        }

